# Programa para convertir grados Celsius a Fahrenheit

# Solicita al usuario que ingrese la temperatura en Celsius
celsius = float(input("Ingresa la temperatura en grados Celsius: "))

# Convierte a Fahrenheit usando la f√≥rmula: F = C * 9/5 + 32
fahrenheit = celsius * 9/5 + 32

# Muestra el resultado
print(f"{celsius} grados Celsius equilvalen a {fahrenheit} grados Fahrenheit. ")